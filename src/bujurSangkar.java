public class bujurSangkar {
    public static void main(String[] args){
        bujursangkar pol = new bujursangkar();
        pol.hitungKeliling();
    }

    public static class bujursangkar {
        public int sisi = 12;
        public int hasil = 4 * sisi;

        public void hitungKeliling() {
            System.out.println("Keliling Bujur sangkar = " + hasil + "cm");
        }
    }

}